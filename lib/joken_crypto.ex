defmodule JokenCrypto do
  @moduledoc """
  Joken Crypto is a library that tries to bring a more meaningful and modern API
  to cryptography in Elixir.

  It is divided in three main categories plus auxiliary tools:
    - public-key cryptography;
    - secrec-key cryptography;
    - one-way cryptography;
    - key handling;
    - utilities (randoms, IVs, etc);

  The target is to provide an easier to use cryptographic library in Elixir:
    - Help interfacing with `:crypto` and `:public_key`
    - Pure Elixir syntax (no records)
    - Able to customize and extend through behaviours
  """
end
